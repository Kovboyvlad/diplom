def run_pipeline(requirements: str, diagram_type: str = "class", model: str = "gpt-4o-mini") -> str:
    """
    Запускает трёхагентный пайплайн CrewAI.

    :param requirements: текст требований к системе
    :param diagram_type: тип диаграммы ('class', 'sequence', 'component', 'activity')
    :param model: ID модели для LiteLLM/CrewAI
    :return: PlantUML-код диаграммы (строка)
    """
    llm = LLM(model=model)

    analyst = Agent(
        role="Senior Business Analyst",
        goal=(
            "Извлечь из требований полную структуру системы: все сущности с атрибутами и типами, "
            "все перечисления статусов, все события, сценарии ошибок и неявные компоненты."
        ),
        backstory=(
            "Ты опытный бизнес-аналитик. Твой главный принцип: реальные системы всегда сложнее "
            "чем написано в требованиях. Ты всегда дополняешь анализ тем, что заказчик подразумевал "
            "но не сказал явно:\n"
            "- если есть статус объекта — ты выписываешь все его значения как перечисление\n"
            "- если есть действие — ты определяешь метод с сигнатурой\n"
            "- если есть взаимодействие — ты описываешь интерфейс\n"
            "- если упомянута ошибка или исключение — ты описываешь сценарий полностью\n"
            "Ты никогда не упрощаешь и не объединяешь то, что должно быть раздельным."
        ),
        verbose=True,
        allow_delegation=False,
        llm=llm,
    )

    architect = Agent(
        role="Principal System Architect",
        goal=(
            "Спроектировать детальную архитектуру: все блоки с атрибутами и методами, "
            "точные типы всех связей, группировка по подсистемам, enum для каждого статуса, "
            "интерфейсы для внешних взаимодействий."
        ),
        backstory=(
            "Ты главный архитектор с экспертизой в UML и SysML. Твои принципы:\n"
            "- каждый статус или тип — это отдельный enum, не строка\n"
            "- каждое внешнее взаимодействие проходит через явный интерфейс\n"
            "- связанные компоненты группируются в подсистемы\n"
            "- тип каждой связи указывается явно: не 'связан', а 'композиция' или 'агрегация'\n"
            "- ты не объединяешь разные сущности в одну ради упрощения\n"
            "Ты проектируешь столько блоков, сколько реально нужно системе — "
            "никогда не упрощаешь архитектуру в угоду краткости."
        ),
        verbose=True,
        allow_delegation=False,
        llm=llm,
    )

    coder = Agent(
        role="PlantUML Expert Engineer",
        goal=(
            f"Написать полный и валидный код PlantUML для диаграммы типа '{diagram_type}'. "
            "Диаграмма должна отражать всю архитектуру без упрощений."
        ),
        backstory=(
            "Ты эксперт по PlantUML. Твои правила абсолютны:\n"
            "- ты пишешь ТОЛЬКО код — никакого текста, пояснений или markdown вокруг\n"
            "- ты переносишь в код ВСЕ блоки из архитектуры, ни один не пропускаешь\n"
            "- каждый атрибут имеет тип, каждый метод имеет скобки\n"
            "- ты не упрощаешь диаграмму: если в архитектуре 10 блоков — в коде 10 блоков"
        ),
        verbose=True,
        allow_delegation=False,
        llm=llm,
    )

    task1 = Task(
        description=(
            f"Проанализируй текст требований:\n\n{requirements}\n\n"
            "Твой анализ должен содержать:\n"
            "1. Все сущности с полным списком атрибутов и их типами данных\n"
            "2. Все перечисления: для каждого статуса, типа, режима — полный список значений\n"
            "3. Все методы и операции каждой сущности\n"
            "4. Все события и сигналы между компонентами\n"
            "5. Сценарии ошибок и исключительных ситуаций\n"
            "6. Неявные сущности — очевидно необходимые, но не упомянутые в тексте"
        ),
        expected_output=(
            "Структурированный анализ: все сущности с атрибутами и типами, "
            "все enum-перечисления со значениями, все методы, все события, сценарии ошибок."
        ),
        agent=analyst,
    )

    task2 = Task(
        description=(
            "На основе анализа спроектируй архитектуру системы.\n\n"
            "Архитектура должна содержать:\n"
            "1. Все блоки с атрибутами (с типами) и методами — не упрощай, не объединяй\n"
            "2. Для каждой связи — явный тип: композиция / агрегация / зависимость / реализация / ассоциация\n"
            "3. Группировку блоков по подсистемам с названиями пакетов\n"
            "4. Отдельные enum-классы для всех статусов и перечислений\n"
            "5. Интерфейсы для всех точек взаимодействия с внешними компонентами\n"
            "6. Бизнес-ограничения (constraints) в виде явных правил"
        ),
        expected_output=(
            "Полная архитектура: все блоки с атрибутами и методами, "
            "типы всех связей явно указаны, блоки сгруппированы в подсистемы, "
            "enum-классы для статусов, интерфейсы для внешних взаимодействий."
        ),
        agent=architect,
    )

    task3 = Task(
        description=CODER_PROMPTS[diagram_type],
        expected_output="Только код PlantUML — от @startuml до @enduml, без какого-либо текста вокруг.",
        agent=coder,
    )

    crew = Crew(
        agents=[analyst, architect, coder],
        tasks=[task1, task2, task3],
        verbose=True,
        process=Process.sequential,
        output_log_file="agent_thoughts.log",
    )

    result = crew.kickoff()
    return str(result)
