def run_single_agent(requirements: str, diagram_type: str = "class", model: str = "gpt-4o-mini") -> tuple[str, str]:
    """
    Запускает одиночного агента-кодера без Analyst и Architect (baseline для сравнения).
    Возвращает (PlantUML-код, пустую строку critique).
    """
    llm = LLM(model=model)

    coder = Agent(
        role="PlantUML Expert Engineer",
        goal=(
            f"По описанию системы напиши полный и валидный PlantUML-код для диаграммы типа '{diagram_type}'."
        ),
        backstory=(
            "Ты эксперт по PlantUML. Твои правила абсолютны:\n"
            "- ты пишешь ТОЛЬКО код — никакого текста, пояснений или markdown вокруг\n"
            "- каждый атрибут имеет тип, каждый метод имеет скобки\n"
            "- диаграмма должна отражать все ключевые сущности из описания"
        ),
        verbose=True,
        allow_delegation=False,
        llm=llm,
    )

    task = Task(
        description=(
            f"На основе описания системы напиши PlantUML-код — диаграмму типа '{diagram_type}'.\n\n"
            f"ОПИСАНИЕ СИСТЕМЫ:\n{requirements}\n\n"
            + CODER_PROMPTS[diagram_type]
        ),
        expected_output="Только код PlantUML — от @startuml до @enduml, без какого-либо текста вокруг.",
        agent=coder,
    )

    crew = Crew(
        agents=[coder],
        tasks=[task],
        verbose=True,
        process=Process.sequential,
        output_log_file="agent_thoughts.log",
    )

    crew.kickoff()
    usage = _extract_usage(crew, model)
    return task.output.raw, "", usage


def run_pipeline(requirements: str, diagram_type: str = "class", model: str = "gpt-4o-mini") -> tuple[str, str]:
    """
    Запускает четырёхагентный пайплайн CrewAI.

    :param requirements: текст требований к системе
    :param diagram_type: тип диаграммы ('class', 'sequence', 'component', 'activity')
    :param model: ID модели для LiteLLM/CrewAI
    :return: кортеж (PlantUML-код диаграммы, замечания критика)
    """
    llm = LLM(model=model)

    analyst = Agent(
        role="Senior Business Analyst",
        goal=(
            "Точно извлечь из требований только то, что явно в них написано: "
            "сущности, их атрибуты, статусы, действия и взаимодействия между ними."
        ),
        backstory=(
            "Ты опытный бизнес-аналитик. Твой главный принцип: строгое следование тексту требований.\n"
            "- ты извлекаешь только то, что явно упомянуто — не додумываешь и не расширяешь\n"
            "- если в требованиях есть статус объекта — выписываешь его значения как перечисление\n"
            "- если упомянуто действие — определяешь метод с сигнатурой\n"
            "- если упомянуто взаимодействие между компонентами — описываешь его\n"
            "- ты НЕ добавляешь инфраструктуру, сервисы, события или исключения "
            "если они явно не указаны в требованиях\n"
            "Твоя задача — точная модель того что написано, не архитектурное видение системы."
        ),
        verbose=True,
        allow_delegation=False,
        llm=llm,
    )

    architect = Agent(
        role="Principal System Architect",
        goal=(
            "Спроектировать архитектуру строго на основе анализа: только те блоки и связи, "
            "которые следуют из требований. Без добавления инфраструктуры и паттернов сверх задания."
        ),
        backstory=(
            "Ты главный архитектор с экспертизой в UML и SysML. Твои принципы:\n"
            "- ты проектируешь ТОЛЬКО то, что есть в требованиях — не добавляешь слои, "
            "сервисы, события или исключения которых там нет\n"
            "- каждый статус или тип — отдельный enum со значениями из требований\n"
            "- тип каждой связи указывается явно: композиция, агрегация, зависимость, ассоциация\n"
            "- каждый enum подключается к классу явной стрелкой, не только как тип атрибута\n"
            "- связанные блоки группируются в пакеты по смыслу из требований\n"
            "Количество блоков определяется требованиями — не больше и не меньше."
        ),
        verbose=True,
        allow_delegation=False,
        llm=llm,
    )

    coder = Agent(
        role="PlantUML Expert Engineer",
        goal=(
            f"Написать полный и валидный код PlantUML для диаграммы типа '{diagram_type}'. "
            "Диаграмма должна отражать всю архитектуру без упрощений."
        ),
        backstory=(
            "Ты эксперт по PlantUML. Твои правила абсолютны:\n"
            "- ты пишешь ТОЛЬКО код — никакого текста, пояснений или markdown вокруг\n"
            "- ты переносишь в код ВСЕ блоки из архитектуры, ни один не пропускаешь\n"
            "- каждый атрибут имеет тип, каждый метод имеет скобки\n"
            "- ты не упрощаешь диаграмму: если в архитектуре 10 блоков — в коде 10 блоков"
        ),
        verbose=True,
        allow_delegation=False,
        llm=llm,
    )

    critic = Agent(
        role="UML Diagram Critic",
        goal=(
            "Критически оценить сгенерированную PlantUML-диаграмму: "
            "выявить пропущенные элементы, ошибки нотации и слабые места промптов."
        ),
        backstory=(
            "Ты строгий эксперт по UML и системному анализу. Твоя задача — не переписывать, "
            "а объективно оценить результат работы предыдущих агентов:\n"
            "- ты сравниваешь диаграмму с исходными требованиями и находишь пропуски\n"
            "- ты проверяешь корректность UML-нотации для данного типа диаграммы\n"
            "- ты оцениваешь полноту: все ли сущности, связи, атрибуты из требований попали в диаграмму\n"
            "- ты формулируешь конкретные рекомендации: что добавить в промпты агентов, "
            "как уточнить входные данные, какая модель лучше справилась бы с этой задачей\n"
            "Ты не хвалишь — ты указываешь на проблемы и пути их устранения."
        ),
        verbose=True,
        allow_delegation=False,
        llm=llm,
    )

    task1 = Task(
        description=(
            f"Проанализируй текст требований:\n\n{requirements}\n\n"
            "Твой анализ должен содержать:\n"
            "1. Все сущности с полным списком атрибутов и их типами данных\n"
            "2. Все перечисления: для каждого статуса, типа, режима — полный список значений\n"
            "3. Все методы и операции каждой сущности\n"
            "4. Все события и сигналы между компонентами\n"
            "5. Сценарии ошибок и исключительных ситуаций\n"
            "6. Неявные сущности — очевидно необходимые, но не упомянутые в тексте"
        ),
        expected_output=(
            "Структурированный анализ: все сущности с атрибутами и типами, "
            "все enum-перечисления со значениями, все методы, все события, сценарии ошибок."
        ),
        agent=analyst,
    )

    task2 = Task(
        description=(
            "На основе анализа спроектируй архитектуру системы.\n\n"
            "Архитектура должна содержать:\n"
            "1. Все блоки с атрибутами (с типами) и методами — не упрощай, не объединяй\n"
            "2. Для каждой связи — явный тип: композиция / агрегация / зависимость / реализация / ассоциация\n"
            "3. Группировку блоков по подсистемам с названиями пакетов\n"
            "4. Отдельные enum-классы для всех статусов и перечислений\n"
            "5. Интерфейсы для всех точек взаимодействия с внешними компонентами\n"
            "6. Бизнес-ограничения (constraints) в виде явных правил"
        ),
        expected_output=(
            "Полная архитектура: все блоки с атрибутами и методами, "
            "типы всех связей явно указаны, блоки сгруппированы в подсистемы, "
            "enum-классы для статусов, интерфейсы для внешних взаимодействий."
        ),
        agent=architect,
    )

    task3 = Task(
        description=CODER_PROMPTS[diagram_type],
        expected_output="Только код PlantUML — от @startuml до @enduml, без какого-либо текста вокруг.",
        agent=coder,
    )

    task4 = Task(
        description=(
            f"Ты получил исходные требования к системе и сгенерированный PlantUML-код диаграммы типа '{diagram_type}'.\n\n"
            f"ИСХОДНЫЕ ТРЕБОВАНИЯ:\n{requirements}\n\n"
            "Проверь диаграмму по следующим критериям и выдай структурированный отчёт в формате Markdown:\n\n"
            "## 1. Пропущенные элементы\n"
            "Перечисли сущности, связи или атрибуты из требований, которые не попали в диаграмму.\n\n"
            "## 2. Ошибки UML-нотации\n"
            "Укажи конкретные строки или блоки с некорректным использованием нотации PlantUML "
            f"для диаграммы типа '{diagram_type}'.\n\n"
            "## 3. Рекомендации по улучшению\n"
            "Конкретные предложения:\n"
            "- что добавить или уточнить в промптах агентов (Analyst, Architect, Coder)\n"
            "- как улучшить входные требования для более качественного результата\n"
            "- замечания по выбору модели (если применимо)\n\n"
            "## 4. Общая оценка\n"
            "Кратко: насколько диаграмма соответствует требованиям (0–10) и почему."
        ),
        expected_output=(
            "Структурированный Markdown-отчёт с разделами: пропущенные элементы, "
            "ошибки нотации, рекомендации по улучшению промптов, общая оценка."
        ),
        agent=critic,
    )

    task5 = Task(
        description=(
            f"Ты получил оригинальный PlantUML-код диаграммы типа '{diagram_type}' "
            "и список замечаний от агента-критика.\n\n"
            "Исправь диаграмму, устранив ВСЕ замечания критика:\n"
            "- добавь пропущенные сущности, атрибуты и связи\n"
            "- исправь ошибки UML-нотации\n"
            "- не удаляй то, что уже было правильно\n\n"
            "НЕ пиши никаких пояснений — только исправленный код от @startuml до @enduml."
        ),
        expected_output="Только исправленный код PlantUML — от @startuml до @enduml, без какого-либо текста вокруг.",
        agent=coder,
    )

    crew = Crew(
        agents=[analyst, architect, coder, critic],
        tasks=[task1, task2, task3, task4, task5],
        verbose=True,
        process=Process.sequential,
        output_log_file="agent_thoughts.log",
    )

    crew.kickoff()
    diagram = task5.output.raw
    critique = task4.output.raw
    usage = _extract_usage(crew, model)
    return diagram, critique, usage


